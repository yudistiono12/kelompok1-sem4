            <p>Buku teks merupakan koleksi yang dipinjamkan kepada anggota Perpustakaan atau biasa disebut dengan koleksi Sirkulasi.</p>
            <p><strong>Buku Referensi</strong></p>
            <p>Koleksi Referensi berupa koleksi rujukan, baik berupa koleksi rujukan   umum  maupun khusus, seperti kamus, ensiklopedia, direktori, abstraks, indeks, manual, laporan tahunan, <em>standard</em>, paten, terbitan pemerintah seperti UU, Keppres, Kepmen, Perpu, Perda dan lain sebagainya. Koleksi referensi dilayankan untuk dibaca di tempat atau difotokopi. </p>
            <p><strong>Terbitan Berkala</strong></p>
            <p>Merupakan koleksi yang berupa sumber informasi yang diterbitkan secara berkala baik harian, mingguan, tengah bulanan, bulanan, triwulanan, tengah tahunan maupun tahunan. Contoh koleksi terbitan berkala antara lain surat  kabar, majalah, jurnal, buletin, dan lain sebagainya. Seperti halnya koleksi referensi, terbitan berkala hanya boleh dibaca ditempat atau difotokopi.</p>
            <p>Koleksi terbitan berkala hasil publikasi di lingkungan Polije selain tersedia dalam <em>format</em> cetak juga dalam bentuk <em>digital</em>. Penelusuran <em>online </em>terhadap jurnal dapat dilakukan melalui <a href="https://e-library.polije.ac.id/index.php?title=&author=&subject=&isbn=&colltype=E-jurnal&location=0&gmd=0&searchtype=advance&search=search">http://e-library.polije.ac.id/</a>. Untuk mendapatkan <em>fulltext-</em>nya pemustaka dapat menghubungi perpustakaan atau berkunjung langsung karena akses <em>fulltext</em> disediakan terbatas dalam jaringan lokal perpustakaan.</p>
            <p><strong>Karya Akhir, Skripsi, Tesis, dan Disertasi<br />
            </strong></p>
            <p>Koleksi ini merupakan kumpulan tugas akhir dari mahasiswa Sekolah Vokasi (D3), Sarjana (S1), Sekolah Pascasarjana (S2) dan Program Doktoral (S3) dari seluruh program studi yang ada di Polije. Keseluruhan koleksi tersedia dalam bentuk <em>soft copy</em>. Karya Akhir, Skripsi, Tesis dan Disertasi hanya dapat dibaca di tempat melalui jaringan komputer pada ruangan ini. Pemustaka diperbolehkan memfotokopi pada bab atau halaman tertentu sesuai peraturan yang berlaku.</p>
